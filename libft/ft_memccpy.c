#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned int	e;
	unsigned char	*d;
	unsigned char	*s;

	if (dst == 0 && src == 0)
		return (0);
	d = (unsigned char *) dst;
	s = (unsigned char *) src;
	e = 0;
	while (n > e)
	{
		d[e] = (unsigned char) s[e];
		if (s[e] == (unsigned char) c)
			return ((void *) &dst[e + 1]);
		e++;
	}
	return (0);
}
