#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	c;
	unsigned char	*t;

	c = 0;
	t = (unsigned char *)s;
	while (n > c)
	{
		t[c] = 0;
		c++;
	}
}
